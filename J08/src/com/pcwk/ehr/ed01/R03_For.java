package com.pcwk.ehr.ed01;

public class R03_For {
	public static void main(String[] args) {
		
		for(int i=1,j=10;i<=10;i++,j--) {
			System.out.printf("i=%2d \t j=%2d%n",i,j);
		}
		
	}
}
