package com.pcwk.ehr.ed04;

public class R08_Operator {

	public static void main(String[] args) {
		//10 절대값 처리를 한다. 
		//나머지 연산을 한다.
		//10에 부호를 붙인다.
		System.out.println(10%8);  //2
		System.out.println(10%-8); //2
		System.out.println(-10%8); //-2
		System.out.println(-10%-8);//-2
	}

}
